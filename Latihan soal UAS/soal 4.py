# Inisialisasi Tuple
t = (3, 7, 3, 9, 3)

# 1. Mengakses dua elemen pertama (index 0 dan 1)
Elemen_pertama_3 = t[0]
Elemen_Kedua_7 = t[1]

# 2. Menghitung jumlah kemunculan angka 3
jumlah_3 = t.count(3)

# 3. Mencari indeks dari angka 9
indeks_9 = t.index(9)

# Output
print("Tuple:", t)
print("Elemen pertama     :", Elemen_pertama_3)
print("Elemen kedua       :", Elemen_Kedua_7)
print("Jumlah angka 3     :", jumlah_3)
print("Indeks angka 9     :", indeks_9)
